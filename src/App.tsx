import { QueryClient, QueryClientProvider } from "@tanstack/react-query";
import { BrowserRouter, Route, Routes } from "react-router-dom";
import { Toaster as Sonner } from "@/components/ui/sonner";
import { Toaster } from "@/components/ui/toaster";
import { TooltipProvider } from "@/components/ui/tooltip";
import Index from "./pages/Index.tsx";
import TasksRoute from "./pages/TasksRoute.tsx";
import NewTaskRoute from "./pages/NewTaskRoute.tsx";
import DashboardRoute from "./pages/DashboardRoute.tsx";
import ProfileRoute from "./pages/ProfileRoute.tsx";
import NotFound from "./pages/NotFound.tsx";

const queryClient = new QueryClient();

const App = () => (
  <QueryClientProvider client={queryClient}>
    <TooltipProvider>
      <Toaster />
      <Sonner />
      <BrowserRouter>
        <Routes>
          <Route path="/" element={<Index />} />
          <Route path="/tasks" element={<TasksRoute />} />
          <Route path="/new-task" element={<NewTaskRoute />} />
          <Route path="/dashboard" element={<DashboardRoute />} />
          <Route path="/profile" element={<ProfileRoute />} />
          <Route path="*" element={<NotFound />} />
        </Routes>
      </BrowserRouter>
    </TooltipProvider>
  </QueryClientProvider>
);

export default App;
